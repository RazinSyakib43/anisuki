                            <h2 class="section-heading">Jepang Akan Gelar Pemakaman Kenegaraan Shinzo Abe 27 September
                            </h2>
                            <p class="subheading">Published by Razin Syakib on 23 July 2022 - 13:24 WIB</p>
                        </div>
                    </div>
                    <div class="row" data-aos="fade-up" data-aos-once="true" data-aos-delay="200">
                        <div class="col"></div>
                        <div class="col">
                            <img src="img/shinzonews.jpeg" alt="">
                        </div>
                        <div class="col"></div>
                    </div>
                    <div class="row mt-4">
                        <div class="col"></div>
                        <div class="col-10" data-aos="fade-up" data-aos-once="true" data-aos-delay="300">
                            <div class="paragraph mt-2">
                                <p class="subheading-article">Jepang akan menggelar pemakaman kenegaraan untuk mendiang
                                    mantan perdana menteri Shinzo Abe pada 27 September mendatang. Pemerintah Jepang
                                    mengumumkan bahwa upacara pemakaman tersebut bakal digelar di Nippon Budokan,
                                    kawasan
                                    yang kerap dijadikan tempat untuk acara konser dan olahraga bergengsi.
                                </p>
                                <p class="subheading-article">Nippon Budokan juga merupakan tempat pemakaman kenegaraan
                                    terakhir di Jepang digelar pada 1967. Saat itu, Jepang berduka ditinggal mantan PM
                                    Shigaru Yoshida. Sepanjang sejarah, Jepang baru satu kali menggelar pemakaman
                                    kenegaraan
                                    bagi mantan PM
                                    yang sudah tidak menjabat, yaitu Yoshida. Kala itu, Jepang ingin melepas Yoshida
                                    dengan
                                    hormat karena politikus itulah yang memimpin Jepang keluar dari kesulitan setelah
                                    Perang
                                    Dunia II. Kini, Jepang juga ingin memberikan penghormatan tertinggi bagi Abe,
                                    politikus
                                    ulung yang
                                    menjadi PM terlama dalam sejarah Jepang.
                                </p>
                                <p class="subheading-article">Sebagaimana dilansir AFP, juru bicara pemerintah Jepang,
                                    Hirokazu Matsuno, mengatakan bahwa selama berkiprah di kancah politik, Abe meraih
                                    sejumlah pencapaian "yang sangat patut dipuji." Jepang pun bakal mengundang
                                    perwakilan
                                    luar negeri untuk memberikan penghormatan terakhir kepada Abe. Rencana upacara
                                    kenegaraan ini menuai kritik sejumlah pihak yang mempertanyakan penggunaan uang
                                    negara
                                    untuk pemakaman seorang mantan pemimpin.
                                </p>
                                <p class="subheading-article">Selain itu, Abe juga sudah dimakamkan pada 12 Juli lalu
                                    dengan
                                    upacara yang digelar keluarga. Namun, Matsuno memastikan bahwa pemakaman kenegaraan
                                    ini
                                    digelar dengan sederhana di tengah suasana berduka.
                                </p>
                                <p class="subheading-article">Abe meninggal dunia setelah ditembak oleh Tetsuya Yamagami
                                    ketika sedang berpidato di Kota Nara pada 8 Juli lalu. Ia sempat dilarikan ke rumah
                                    sakit, tapi pendarahan yang begitu parah membuat nyawanya tak terselamatkan. Abe
                                    akhirnya mengembuskan napas terakhirnya pada 17.03 waktu setempat, meninggalkan
                                    jejak
                                    kegemilangan masa kepemimpinannya di Jepang.
                                </p>
